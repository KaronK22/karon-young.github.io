// Function 6 - Capitalize All Words /////////////////////////////////
//////////////////////////////////////////////////////////////////////
function capitalizeAllWords(string) {
    splStr = string.split(' ');
    for (var i = 0; i <= splStr.length - 1;i++){
   splStr[i] = splStr[i][0].toUpperCase() + splStr[i].substr(1);
    } 
  return splStr.join(' ');
}

