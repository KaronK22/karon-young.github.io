function min(num1, num2) {
 return Math.min(num1,num2);
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  if ( n < 0){

    n = Math.abs(n)
  }

 if (n === 0){
  return true;
 }
 if (n === 1){
  return false;
 }
 return isEven(n - 2);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let result1 = 0;
  for (let i = 0;i <= string.length; i++){
    if (string[i] === char){
      result1 += 1
    }
  }
   return result1;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
let result = 0;
for (let i = 0;i <= string.length; i++){
  if (string[i] === 'B'){
    result += 1
  }
}
 return result;
}

