// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end) {
  var arr = []
  let cnt = start;
  function increaseCnt(cnt){
    if (cnt >= end){
      return arr.push(end);
    } else {
      arr.push(cnt);
      increaseCnt(++cnt);
    }
  }
  increaseCnt(cnt);
  return arr;
}

